namespace SfDataGridSample
{
	/// <summary>
	/// A way of describing values returned from the service.
	/// </summary>
	public enum ValueType
	{
		/// <summary>
		/// The attribute value is a string type.
		/// </summary>
		String,

		/// <summary>
		/// The attribute value is a DateTime type.
		/// </summary>
		DateTime,

		/// <summary>
		/// The attribute value is floating point.
		/// </summary>
		Double,

		/// <summary>
		/// The attribute value is an integer.
		/// </summary>
		Integer
	}
}
